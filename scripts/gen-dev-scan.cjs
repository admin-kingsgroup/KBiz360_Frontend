/* ════════════════════════════════════════════════════════════════════════════
   scripts/gen-dev-scan.cjs

   Runs the frontend code scanner (scripts/dev-scan/codeScan.cjs) over
   KBiz360_Frontend/src and writes the findings to src/core/devScan.generated.js.

   Runs automatically before `npm run dev` / `npm run build` / `npm test`
   (predev/prebuild/pretest) — right after gen-route-manifest so the route
   cross-checks read a fresh manifest. A guard test (dev-scan.test.js) re-runs
   the scan and fails if the committed file is stale, exactly like the route
   manifest guard.

   WHY a build-time file when the backend already scans live? On production the
   backend EC2 box has no frontend source, so its live scan covers only the API.
   This bundled copy gives the Control Tower ▸ Development lens full FE coverage
   there too. In dev, the backend reaches this tree and the lens prefers its live
   findings over this copy (it keys off scan.roots[].scanned).
   ════════════════════════════════════════════════════════════════════════════ */
'use strict';
const fs = require('fs');
const path = require('path');
const { scanFrontend } = require('./dev-scan/codeScan.cjs');

const ROOT = path.join(__dirname, '..');
const SRC = path.join(ROOT, 'src');
const OUT = path.join(SRC, 'core', 'devScan.generated.js');

function render({ files, findings, counts }) {
  return `/* ════════════════════════════════════════════════════════════════════
   AUTO-GENERATED by scripts/gen-dev-scan.cjs — DO NOT EDIT BY HAND.
   Regenerated before every \`npm run dev\` / \`npm run build\` / \`npm test\`.
   Build-time frontend code-scan findings for the Control Tower ▸ Development
   lens (full FE coverage on production, where the backend can't reach this
   tree live). Rules mirror the backend runtime scanner.
   ════════════════════════════════════════════════════════════════════════════ */

// Files scanned at build time. generatedAt is intentionally omitted (a moving
// timestamp would make every build a spurious diff); the lens shows the live
// scan's timestamp and labels these findings "from last build".
export const DEV_SCAN = ${JSON.stringify({ tree: 'FE', filesScanned: files, counts, findings }, null, 2)};

export default DEV_SCAN;
`;
}

function generate() {
  return render(scanFrontend(SRC));
}

if (require.main === module) {
  const result = scanFrontend(SRC); // scan ONCE — write + log from the same result
  fs.writeFileSync(OUT, render(result));
  // eslint-disable-next-line no-console
  console.log(`✓ dev scan written: ${path.relative(ROOT, OUT)} (${result.counts.total} findings — ${result.counts.bySeverity.high} high)`);
}

module.exports = { generate, scanFrontend, OUT, SRC };
