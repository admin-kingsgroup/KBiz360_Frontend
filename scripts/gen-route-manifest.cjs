/* ════════════════════════════════════════════════════════════════════════════
   scripts/gen-route-manifest.js

   Extracts EVERY navigable route the app can render from src/App.jsx (the legacy
   string-router) + the migrated feature-route tables, and writes them to
   src/core/routeManifest.generated.js. The Page Visibility Control catalogue
   imports that manifest so a user can toggle ANY page — including standalone
   routes that aren't in the nav menus.

   Runs automatically before `npm run dev` / `npm run build` (predev/prebuild) so
   the manifest can never drift; a guard test (routeManifest.test.js) re-runs this
   extraction and fails if the committed file is stale.

   Route forms handled:
     • exact          if(route==="/x")               → "/x"
     • compound       route==="/a" || route==="/b"   → "/a","/b"
     • regex-alts     /^\/p\/(a|b|c)$/.test(route)    → "/p/a","/p/b","/p/c"
     • migrated table { path: '/finance/x', title }  → "/finance/x" (+ label)
   ════════════════════════════════════════════════════════════════════════════ */
'use strict';
const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const APP_JSX = path.join(ROOT, 'src/App.jsx');
const MIGRATED_TABLES = [
  path.join(ROOT, 'src/modules/finance/routes/index.jsx'),
  path.join(ROOT, 'src/modules/reconciliation/routes/index.jsx'),
  path.join(ROOT, 'src/modules/tally-reconciliation/routes/index.jsx'),
  path.join(ROOT, 'src/modules/support/routes/index.jsx'),
];
const OUT = path.join(ROOT, 'src/core/routeManifest.generated.js');

// Pull every route literal + any migrated path/label out of the given sources.
function extractRoutes(appSrc, tableSrcs) {
  const routes = new Set();
  const labels = {};

  // 1) Exact + compound exact matches: route === "…"  (and single-quoted).
  for (const m of appSrc.matchAll(/route\s*===\s*['"]([^'"]+)['"]/g)) {
    if (m[1].startsWith('/')) routes.add(m[1]);
  }

  // 2) Regex-alternation routes: /^\/<prefix>\/(<a|b|c>)$/  (literal prefix only;
  //    multi-alternation prefixes like (sales|purchase) are already covered by
  //    their own single-prefix patterns, so we skip them here).
  for (const m of appSrc.matchAll(/\/\^\\\/([a-z][a-z0-9-]*)\\\/\(([^)]*)\)\$\//gi)) {
    const prefix = m[1];
    for (const alt of m[2].split('|')) {
      const a = alt.trim();
      if (a) routes.add(`/${prefix}/${a}`);
    }
  }

  // 3) Migrated declarative route tables: { path: '…', title: '…' }.
  for (const src of tableSrcs) {
    for (const m of src.matchAll(/path:\s*['"]([^'"]+)['"]\s*,\s*title:\s*['"]([^'"]+)['"]/g)) {
      if (!m[1].startsWith('/')) continue;
      routes.add(m[1]);
      labels[m[1]] = m[2];
    }
  }

  return { routes: [...routes].sort(), labels };
}

function render({ routes, labels }) {
  const sortedLabels = Object.keys(labels).sort().reduce((o, k) => ((o[k] = labels[k]), o), {});
  return `/* ════════════════════════════════════════════════════════════════════
   AUTO-GENERATED by scripts/gen-route-manifest.cjs — DO NOT EDIT BY HAND.
   Regenerated before every \`npm run dev\` / \`npm run build\` (predev/prebuild).
   The complete list of routes App.jsx can render, so Page Visibility Control can
   make every page toggleable — including standalone pages not in the nav menus.
   ════════════════════════════════════════════════════════════════════════════ */

export const APP_ROUTES = ${JSON.stringify(routes, null, 2)};

// Friendly labels for routes that carry one (migrated route tables); everything
// else gets a label derived from its path in core/pageCatalog.js.
export const APP_ROUTE_LABELS = ${JSON.stringify(sortedLabels, null, 2)};
`;
}

function generate() {
  const appSrc = fs.readFileSync(APP_JSX, 'utf8');
  const tableSrcs = MIGRATED_TABLES.map((p) => fs.readFileSync(p, 'utf8'));
  return render(extractRoutes(appSrc, tableSrcs));
}

// Read sources & return the parsed manifest (used by the guard test).
function manifest() {
  const appSrc = fs.readFileSync(APP_JSX, 'utf8');
  const tableSrcs = MIGRATED_TABLES.map((p) => fs.readFileSync(p, 'utf8'));
  return extractRoutes(appSrc, tableSrcs);
}

if (require.main === module) {
  fs.writeFileSync(OUT, generate());
  // eslint-disable-next-line no-console
  console.log(`✓ route manifest written: ${path.relative(ROOT, OUT)} (${manifest().routes.length} routes)`);
}

module.exports = { extractRoutes, generate, manifest, OUT };
